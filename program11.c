// pre,post increment pre,post decrement
#include<stdio.h>
void main()
{
	int a=10,b=13,c=5;
	printf("%d\n",++a);
	printf("%d\n",a++);
	printf("%d\n",a--);
	printf("%d\n",--a);
	printf("\n");
	printf("%d\n",++b);
	printf("%d\n",b++);
	printf("%d\n",b--);
	printf("%d\n",--b);
	printf("\n");
	printf("%d\n",++c);
	printf("%d\n",c++);
	printf("%d\n",c--);
	printf("%d\n",--c);
}
